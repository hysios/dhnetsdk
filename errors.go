func LoginError(errcode int) error {
	switch errcode {
	case 0:
		return errors.New("Login Success")
	case 1:
		return errors.New("Account or Password Incorrect")

	case 2:
		return errors.New("User Is Not Exist")

	case 3:
		return errors.New("Login Timeout")

	case 4:
		return errors.New("Repeat Login")

	case 5:
		return errors.New("User Account is Locked")

	case 6:
		return errors.New("User In Blacklist")

	case 7:
		return errors.New("Device Busy")

	case 8:
		return errors.New("Sub Connect Failed")

	case 9:
		return errors.New("Host Connect Failed")

	case 10:
		return errors.New("Max Connect")

	case 11:
		return errors.New("Support Protocol3 Only")

	case 12:
		return errors.New("UKey Info Error")

	case 13:
		return errors.New("No Authorized")

	case 18:
		return errors.New("Device Account isn't Initialized")

	default:
		return errors.New("Unknown Error")

	}
}

